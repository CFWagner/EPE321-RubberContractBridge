#ifndef CARDSUIT_H
#define CARDSUIT_H

// Represents suit of playing card. Integers are used to represent the value of the suit according to rubber contract bridge.
// Larger integers indicate greater value.

enum CardSuit {
    CLUBS = 0,
    DIAMONDS = 1,
    HEARTS = 2,
    SPADES = 3,
    NONE = 4
};

#endif // CARDSUIT_H
